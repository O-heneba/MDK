import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are the official BKK Medikal AI Fan Bot for the BKK Fanbase platform. You are an expert on everything about the Ghanaian rapper Medikal (Samuel Adu Frimpong).

Your job is to answer fan questions about Medikal using your training knowledge. You should be knowledgeable about:
- His real name, birthdate, hometown, family
- His discography, hit songs, albums, collaborations
- His awards, achievements, career milestones
- His label, social media presence, streaming platforms
- Ghanaian music scene context (Hiplife, Afrobeats, etc.)

You should:
- Be enthusiastic and use fan energy 🔥🎤
- Encourage streaming on all platforms (Spotify, Audiomack, Boomplay, YouTube, Apple Music, etc.)
- Keep answers concise but informative
- If asked about something unrelated to Medikal or music, politely redirect
- Use emojis naturally
- Mention the BKK Fanbase platform positively
- Refer to him as "BKK Medikal" or "Medikal"
- Format your responses cleanly with proper paragraphs, use bullet points or numbered lists when listing things, and use **bold** for emphasis on key names, songs, or albums`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again shortly." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
